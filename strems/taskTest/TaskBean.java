    @Override
    public boolean equals(Object obj) {
        if (obj == null){
            return false;
        }
        if (this == obj){
            return true;
        }
        if (obj instanceof TaskBean){
            TaskBean taskBean = (TaskBean) obj;
            return taskBean.point == this.point && taskBean.status.equals(this.status);
        }
        return false;
    }

